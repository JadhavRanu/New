package controlstatement;

public class Relationaloperater {

	public static void main(String[] args) {
		
		int i =50;
		int j =10;
		System.out.println(i>j);
		
		System.out.println(i<j);
		
		System.out.println(i>=j);
		
		System.out.println(i<=j);
		
		System.out.println(i!=j);
		
		System.out.println(i==j);
	}

}
