package controlstatement;

public class SwitchState {

	public static void main(String[] args) {
		char Text ='P';
		switch (Text) {
		
		case 'Q':System.out.println("Text matched-Q");
			break;
		case 'P':System.out.println("Text matched-P");
		break;
		case 'S':System.out.println("Text matched-S");
		break;
		case 'T':System.out.println("Text matched-T");
			break;
		case 'R':System.out.println("Text matched-R");
			break;
			default:
				System.out.println("Text is not matched");	
		}
	}



	}


