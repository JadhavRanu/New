package controlstatement;

public class Ninetableforloop {

	
	public static void main(String[] args) {
		
		
			
			int number= 9;

			for(int a=1;a<=10;a++) {
				
				System.out.println(number*a);
			}
			
			
		System.out.println("*****************************************");
		
		
		int p=2;
		for(int i=1;i<=10;i++) {
			System.out.println(p*i);
		}
		
		
		
	}
}
