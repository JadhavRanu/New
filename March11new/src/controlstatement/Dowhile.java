package controlstatement;

public class Dowhile {

	
	public static void main(String[] args) {
		
		
		int i=1;
		do {
			System.out.println(i);
			i++;
		}
		while(i<=10);
		
	System.out.println("*********************************");
	
	//infinite do while loop
	
	int a=10;
	do {
		System.out.println(a);
	a--;
	}
	while(a>=1);
	
}}
