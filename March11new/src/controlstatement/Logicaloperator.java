package controlstatement;

public class Logicaloperator {

	public static void main(String[] args) {
		
		boolean a =true;
		boolean b = true;
		
		
		System.out.println(a&&b);
		
		System.out.println(a||b);
		System.out.println();
		
		
		boolean a1 =true;
		boolean b1 = false;
		
		
		System.out.println(a1&&b1);
		
		System.out.println(a1||b1);
		System.out.println();
		
		
		boolean a2 =false;
		boolean b2 = false;
		
		
		System.out.println(a2&&b2);
		
		System.out.println(a2||b2);
		
		
	}

}
